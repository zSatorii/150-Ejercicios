# Calculadora de promedio
nota1 = 8.5 # Primera calificación
nota2 = 9.0 # Segunda calificación
nota3 = 7.5 # Tercera calificación

suma = nota1 + nota2 + nota3 # Sumamos todas las notas
promedio = suma / 3 # Dividimos entre la cantidad de notas
print("Calificaciones:", nota1, nota2, nota3)
print("Promedio:", promedio)