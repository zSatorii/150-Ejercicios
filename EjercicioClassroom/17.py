# Mi lista de colores favoritos
colores = ["azul", "verde", "rojo", "amarillo", "morado"]
print("Mis colores favoritos son:")
print("Lista completa:", colores)
print("Total de colores:", len(colores)) # len() cuenta los elementos
print("\nAccediendo a colores individuales:")
print("Primer color:", colores[0]) # Los índices empiezan en 0
print("Segundo color:", colores[1]) # Segundo elemento
print("Último color:", colores[-1]) # -1 significa el último
print("Penúltimo color:", colores[-2]) # -2 significa el penúltimo